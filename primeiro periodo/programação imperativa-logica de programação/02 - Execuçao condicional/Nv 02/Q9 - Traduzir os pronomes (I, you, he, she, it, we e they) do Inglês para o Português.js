// Traduzir os pronomes (I, you, he, she, it, we e they) do Inglês para o Português;

let pronome = "they";

if (pronome == "i") {
    console.log(pronome + " = " + "eu");
} else if (pronome == "you") {
    console.log(pronome + " = " + "voce")
} else if (pronome == "he") {
    console.log(pronome + " = " + "ele")
} else if (pronome == "she") {
    console.log(pronome + " = " + "ela");
} else if (pronome == "it") {
    console.log(pronome + " = " + "isto");
} else if (pronome == "we") {
    console.log(pronome + " = " + "nos");
} else {
    console.log(pronome + " = " + "eles/elas");
}