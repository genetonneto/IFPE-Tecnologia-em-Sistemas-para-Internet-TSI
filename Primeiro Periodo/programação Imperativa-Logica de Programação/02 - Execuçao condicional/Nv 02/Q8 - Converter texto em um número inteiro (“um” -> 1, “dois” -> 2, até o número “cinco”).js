// Converter texto em um número inteiro (“um” -> 1, “dois” -> 2, até o número “cinco”);

let numero = "um";

if (numero == "um") {
    console.log(1);
} else if (numero == "dois") {
    console.log(2);
} else if (numero == "tres") {
    console.log(3);
} else if (numero == "quatro"){
    console.log(4);
} else {
    console.log(5);
}