// Testar se o quadrado de um número é maior do que ele multiplicado por 10;

var n1 = 8;

if (n1 ** 2 > n1 * 10) {
    console.log("O quadrado de " + n1 + " é maior do que " + n1 + " multiplicado por 10");
} else if (n1 ** 2 == n1 * 10) {
    console.log("O quadrado do " + n1 + " e o " + n1 + " multiplicado por 10 são iguais");
} else {
    console.log(n1 + " ao quadrado é menor que " + n1 + " multiplicado por 10");
}                   