// Receber quatro números como parâmetro e retornar a média dos quatro como resultado;

function media (n1, n2, n3, n4) {
    return (n1 + n2 + n3 + n4) / 4; 
}

let resultado = media (2, 4, 6, 8);
console.log(resultado); 

